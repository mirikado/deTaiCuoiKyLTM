    String answerSelected() {
        if (jRadioButtonA.isSelected()) {
            return "A";
        }
        if (jRadioButtonB.isSelected()) {
            return "B";
        }
        if (jRadioButtonC.isSelected()) {
            return "C";
        }
        return "D";
    }

    public void getData() {
        list = conn.getQuestionsRandom(numQuestion);
    }

    public void getQuestions() {
        jLabelTimer.setText("00:10");
        jLabelQuestion.setText("Question " + (next + 1));
        jLabelQuestionContent.setText(list.get(next).getQuestionContent());
        jRadioButtonA.setText(list.get(next).getA());
        jRadioButtonB.setText(list.get(next).getB());
        jRadioButtonC.setText(list.get(next).getC());
        countDown();
    }

