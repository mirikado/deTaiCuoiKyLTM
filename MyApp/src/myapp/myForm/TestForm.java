    // Timer
    public void countDown() {
        timeLeft = 10000;
        timer = new Timer(1000, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                timeLeft -= 1000;
                SimpleDateFormat df = new SimpleDateFormat("mm:ss");
                jLabelTimer.setText(df.format(timeLeft));
                if (timeLeft <= 0) {
                    if (next == numQuestion - 1) {
                        timer.stop();
                        finalTest();
                    } else {
                        nextQuestion();
                    }
                }
            }
        });
        timer.start();
    }

    // stop Timer
    void nextQuestion() {
        timer.stop();
        answer[next] = answerSelected();
        resetRadioButton();
        next++;
        if (next == numQuestion - 1) {
            jButtonNext.setEnabled(false);
        }
        getQuestions();
    }

    void finalTest() {
        this.dispose();
        for (int i = 0; i < numQuestion; i++) {
            if (list.get(i).getResult().equalsIgnoreCase(answer[i])) {
                score++;
            }
        }
        System.out.println("Score: " + (score * (10 / (float) numQuestion)));
        score = (score * (10 / (float) numQuestion));
        JOptionPane.showMessageDialog(rootPane, "Score: " + score);
        this.dispose();
        new Main().setVisible(true);
    }

